t3=[r3, d33; 0 0 0 1];
t4=[r4, d44; 0 0 0 1];
t5=[r5, d55; 0 0 0 1];
t6=[r6, d66; 0 0 0 1];
t7=[r7, d77; 0 0 0 1];
t8=[r8, d88; 0 0 0 1];
t9=[r9, d99; 0 0 0 1];
T=t1*t2*t3*t4*t5*t6*t7*t8*t9
