r2=[cos(a2) -sin(a2) 0; sin(a2) cos(a2) 0; 0 0 1];
r3=[cos(a3) -sin(a3) 0; sin(a3) cos(a3) 0; 0 0 1];
r4=[cos(a4) -sin(a4) 0; sin(a4) cos(a4) 0; 0 0 1];
r5=[cos(a5) -sin(a5) 0; sin(a5) cos(a5) 0; 0 0 1];
r6=[cos(a6) -sin(a6) 0; sin(a6) cos(a6) 0; 0 0 1];
r7=[cos(a7) -sin(a7) 0; sin(a7) cos(a7) 0; 0 0 1];
r8=[cos(a8) -sin(a8) 0; sin(a8) cos(a8) 0; 0 0 1];
r9=[cos(a9) -sin(a9) 0; sin(a9) cos(a9) 0; 0 0 1];
d1=[l1;0;0];
d2=[l2;0;0];
d3=[l3;0;0];
d4=[l4;0;0];
d5=[l5;0;0];
d6=[l6;0;0];
d7=[l7;0;0];
d8=[l8;0;0];
d9=[l9;0;0];
